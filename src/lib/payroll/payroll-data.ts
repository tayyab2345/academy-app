function buildBreakdownSummary(
  grossAmount: Prisma.Decimal | number,
  paidAmount: Prisma.Decimal | number,
  adjustments: Array<{
    type: PayrollAdjustmentType
    amount: Prisma.Decimal | number
  }>
) {
  const breakdown = calculatePayrollBreakdownTotals(
    toNumber(grossAmount),
    adjustments.map((adjustment) => ({
      type: adjustment.type,
      amount: toNumber(adjustment.amount),
    }))
  )

  const outstandingAmount = roundPayrollAmount(
    Math.max(breakdown.netPayable - toNumber(paidAmount), 0)
  )

  return {
    breakdown,
    outstandingAmount,
  }
}

function mapCurrencyTotals(source: Map<string, number>) {
  return Array.from(source.entries())
    .map(([currency, amount]) => ({
      currency,
      amount: roundPayrollAmount(amount),
    }))
    .sort((left, right) => left.currency.localeCompare(right.currency))
}

function summarizeRecords(
  records: Array<{
    grossAmount: Prisma.Decimal | number
    paidAmount: Prisma.Decimal | number
    currency: string
    status: PayrollStatus
    isFinalized: boolean
    adjustments: Array<{
      type: PayrollAdjustmentType
      amount: Prisma.Decimal | number
    }>
  }>
): PayrollSummaryTotals {
  const baseByCurrency = new Map<string, number>()
  const netByCurrency = new Map<string, number>()
  const paidByCurrency = new Map<string, number>()
  const outstandingByCurrency = new Map<string, number>()
  const bonusesByCurrency = new Map<string, number>()
  const deductionsByCurrency = new Map<string, number>()
  const advancesByCurrency = new Map<string, number>()

  const summary: PayrollSummaryTotals = {
    totalRecords: records.length,
    pendingCount: 0,
    partialCount: 0,
    paidCount: 0,
    finalizedCount: 0,
    baseByCurrency: [],
    netByCurrency: [],
    paidByCurrency: [],
    outstandingByCurrency: [],
    bonusesByCurrency: [],
    deductionsByCurrency: [],
    advancesByCurrency: [],
  }

  for (const record of records) {
    const currency = record.currency
    const { breakdown, outstandingAmount } = buildBreakdownSummary(
      record.grossAmount,
      record.paidAmount,
      record.adjustments
    )

    baseByCurrency.set(
      currency,
      (baseByCurrency.get(currency) || 0) + breakdown.baseSalary
    )
    netByCurrency.set(
      currency,
      (netByCurrency.get(currency) || 0) + breakdown.netPayable
    )
    paidByCurrency.set(
      currency,
      (paidByCurrency.get(currency) || 0) + toNumber(record.paidAmount)
    )
    outstandingByCurrency.set(
      currency,
      (outstandingByCurrency.get(currency) || 0) + outstandingAmount
    )
    bonusesByCurrency.set(
      currency,
      (bonusesByCurrency.get(currency) || 0) + breakdown.totalBonuses
    )
    deductionsByCurrency.set(
      currency,
      (deductionsByCurrency.get(currency) || 0) + breakdown.totalDeductions
    )
    advancesByCurrency.set(
      currency,
      (advancesByCurrency.get(currency) || 0) +
        breakdown.totalAdvanceAdjustments
    )

    if (record.status === PayrollStatus.pending) {
      summary.pendingCount += 1
    } else if (record.status === PayrollStatus.partial) {
      summary.partialCount += 1
    } else {
      summary.paidCount += 1
    }

    if (record.isFinalized) {
      summary.finalizedCount += 1
    }
  }

  summary.baseByCurrency = mapCurrencyTotals(baseByCurrency)
  summary.netByCurrency = mapCurrencyTotals(netByCurrency)
  summary.paidByCurrency = mapCurrencyTotals(paidByCurrency)
  summary.outstandingByCurrency = mapCurrencyTotals(outstandingByCurrency)
  summary.bonusesByCurrency = mapCurrencyTotals(bonusesByCurrency)
  summary.deductionsByCurrency = mapCurrencyTotals(deductionsByCurrency)
  summary.advancesByCurrency = mapCurrencyTotals(advancesByCurrency)

  return summary
}

