export async function getPayrollStaffOptions(
  academyId: string,
  includeUserIds: string[] = []
): Promise<PayrollStaffOption[]> {
  const users = await prisma.user.findMany({
    where: buildStaffWhere(academyId, includeUserIds),
    select: {
      id: true,
      firstName: true,
      lastName: true,
      email: true,
      phone: true,
      avatarUrl: true,
      role: true,
      isActive: true,
      teacherProfile: {
        select: {
          employeeId: true,
        },
      },
      compensationProfile: {
        select: {
          id: true,
          amount: true,
          currency: true,
          isActive: true,
          effectiveFrom: true,
          notes: true,
        },
      },
    },
  })

  return users
    .filter((user) => user.role === Role.admin || user.role === Role.teacher)
    .map((user) => ({
      ...toBaseStaffMember(user),
      compensationProfile: user.compensationProfile
        ? {
            id: user.compensationProfile.id,
            amount: roundPayrollAmount(toNumber(user.compensationProfile.amount)),
            currency: user.compensationProfile.currency,
            isActive: user.compensationProfile.isActive,
            effectiveFrom: user.compensationProfile.effectiveFrom.toISOString(),
            notes: user.compensationProfile.notes,
          }
        : null,
    }))
    .sort((left, right) => {
      if (left.isActive !== right.isActive) {
        return left.isActive ? -1 : 1
      }

      if (left.role !== right.role) {
        return getRoleSortOrder(left.role) - getRoleSortOrder(right.role)
      }

      const firstNameComparison = left.firstName.localeCompare(right.firstName)

      if (firstNameComparison !== 0) {
        return firstNameComparison
      }

      return left.lastName.localeCompare(right.lastName)
    })
}

